Locations = ["Iran" , "Russia" , "Turkey" , "Brazil" , "France"]

print("Original order:")
print(Locations)

print("\nAlphabetical:")
print(sorted(Locations))

print("\nOriginal order:")
print(Locations)

print("\nReverse alphabetical : ")
print(sorted(Locations, reverse=True))

print("\nOriginal order:")
print(Locations)

print("\nReversed:")
Locations.reverse()
print(Locations)

print("\nOriginal order:")
Locations.reverse()
print(Locations)

print("\nAlphabetical")
Locations.sort()
print(Locations)

print("\nReverse alphabetical")
Locations.sort(reverse=True)
print(Locations)